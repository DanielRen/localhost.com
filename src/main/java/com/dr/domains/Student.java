package com.dr.domains;

public class Student {
	String id;
	String name;
	String age;
	
	public String getId(){
		return this.id;
	}
	public void setId(String id){
		this.id = id;
	}
	
	public String getName(){
		return this.name;
	}
	public void setName(String name){
		this.name = name;
	}
	
	public String getAge(){
		return this.age;
	}
	public void setAge(String age){
		this.age = age;
	}
}
